export const mapDeliveryType = (type) => {
  const types = { 送货安装: 1, 送货到家: 2, 送货到楼下: 3, 客户自提: 4 };
  if (typeof type !== "number") {
    return types[type];
  } else {
    for (const key in types) {
      if (type === types[key]) {
        return key;
      }
    }
  }
};

